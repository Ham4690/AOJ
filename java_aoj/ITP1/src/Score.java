public class Score {
    private int math;
    private int science;
    public Score(int math, int science) {
        this.math = math;
        this.science = science;
    }

    public int getMath() {
        return math;
    }

    public void setMath(int math) {
        this.math = math;
    }

    public int getScience() {
        return science;
    }

    public void setScience(int science) {
        this.science = science;
    }
}
